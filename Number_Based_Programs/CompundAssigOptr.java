package com_jspider.logicalProgramming;

public class CompundAssigOptr {

	public static void main(String[] args) {
		int a;
		
		a=10;//Simple Assignment opr
		System.out.println(a);
		
		a+=10;//Compound Assignment opr(a=a+10)->10+10=20
		System.out.println(a);
		
		a-=10;//Compound Assignment opr(a=a-10)->20-10=10
		System.out.println(a);
		
	}

	
}
